//! SystemVerilog pretty-printer. Assumes IR invariants hold; does not
//! validate.
//!
//! Synchronous-design discipline: a single CLK (posedge) and a single
//! RST_N (async, active-low) drive every flop in the module.

use crate::config::SvVersion;
use crate::ir::{
    AggregateGroup, AggregateKind, Design, Direction, Flop, ForFoldKind, GateOp, InstanceId,
    Module, Node, NodeId, Port, PortId,
};
use std::collections::BTreeMap;
use std::collections::BTreeSet;
use std::fmt::Write;
use tracing::{debug, info, instrument};

#[instrument(level = "info", skip(m), fields(module = %m.name))]
pub fn to_sv(m: &Module) -> String {
    to_sv_versioned(m, SvVersion::default())
}

/// `SV-VERSION-TARGETING.2b.1` — emit a single module targeting a chosen
/// IEEE 1800 standard. `sv_version` is the down-gating capability bound,
/// threaded to the construct sites (`to_sv_with_modules`); the whole
/// current emitted subset is 1800-2012-valid, so every target produces
/// byte-identical output today (the down-gating guarantee over the current
/// subset — `.3` is the first version-distinctive up-opt). `to_sv`
/// delegates here with `SvVersion::default()`, so existing callers are
/// byte-identical.
#[instrument(level = "info", skip(m), fields(module = %m.name, sv_version = ?sv_version))]
pub fn to_sv_versioned(m: &Module, sv_version: SvVersion) -> String {
    assert!(
        m.instances.is_empty(),
        "hierarchical modules require emit::to_sv_in_design or emit::to_sv_design"
    );
    to_sv_with_modules(m, None, sv_version)
}

#[instrument(level = "info", skip(m, design), fields(module = %m.name))]
pub fn to_sv_in_design(m: &Module, design: &Design) -> String {
    to_sv_in_design_versioned(m, design, SvVersion::default())
}

/// `SV-VERSION-TARGETING.2b.1` — version-aware counterpart of
/// `to_sv_in_design`. See `to_sv_versioned`.
#[instrument(level = "info", skip(m, design), fields(module = %m.name, sv_version = ?sv_version))]
pub fn to_sv_in_design_versioned(m: &Module, design: &Design, sv_version: SvVersion) -> String {
    let modules = build_module_lookup(design);
    to_sv_with_modules(m, Some(&modules), sv_version)
}

#[instrument(level = "info", skip(design), fields(top = %design.top))]
pub fn to_sv_design(design: &Design) -> String {
    to_sv_design_versioned(design, SvVersion::default())
}

/// `SV-VERSION-TARGETING.2b.1` — version-aware counterpart of
/// `to_sv_design`. See `to_sv_versioned`.
#[instrument(level = "info", skip(design), fields(top = %design.top, sv_version = ?sv_version))]
pub fn to_sv_design_versioned(design: &Design, sv_version: SvVersion) -> String {
    let modules = build_module_lookup(design);
    let mut out = String::new();
    for (idx, module) in design.modules.iter().enumerate() {
        if idx > 0 {
            writeln!(out).unwrap();
            writeln!(out).unwrap();
        }
        out.push_str(&to_sv_with_modules(module, Some(&modules), sv_version));
    }
    out
}

fn build_module_lookup(design: &Design) -> BTreeMap<&str, &Module> {
    design
        .modules
        .iter()
        .map(|module| (module.name.as_str(), module))
        .collect()
}

fn to_sv_with_modules(
    m: &Module,
    modules: Option<&BTreeMap<&str, &Module>>,
    sv_version: SvVersion,
) -> String {
    // `sv_version` is the threaded down-gating capability bound
    // (`SV-VERSION-TARGETING.2b.1`): construct-emission sites consult
    // `sv_version.permits(introduced_standard)` before emitting a
    // version-distinctive construct. Today every construct below is
    // 1800-2012-valid, so the bound is real but vacuous and every target
    // emits byte-identical text; the first up-opt site lands at `.3`.
    //
    // Emitter is a dumb serialiser. It assumes all IR invariants —
    // including Rule 18 (no orphan gates) — were enforced upstream
    // during construction. The emitter walks `m.nodes` in order and
    // writes a wire declaration + assign for each Gate, plus a flop
    // declaration + always_ff branch for each Flop, plus explicit
    // child-module instantiations and output drives. No filtering, no
    // reachability checks.
    let names = build_names(m);
    let instance_outputs = build_instance_output_index(m);
    info!(
        gates = m
            .nodes
            .iter()
            .filter(|n| matches!(n, Node::Gate { .. }))
            .count(),
        flops = m.flops.len(),
        instances = m.instances.len(),
        inputs = m.inputs.len(),
        outputs = m.outputs.len(),
        sv_version = ?sv_version,
        "✍️ emit SV"
    );
    let mut out = String::new();
    writeln!(out, "// Generated by anvil. Module: {}", m.name).unwrap();
    // Phase 5b: packed-aggregate emitter projection
    // (`PHASE-5B-AGGREGATES.2.1`). When present, a contiguous
    // same-direction data-port group is rendered as one packed
    // `struct` typedef + a single aggregate port + boundary alias
    // wires/assigns; the flat IR body below is byte-identical.
    // `aggregate_layout == None` (default-off) ⇒ byte-identical.
    if let Some(layout) = &m.aggregate_layout {
        if let Some(g) = &layout.inputs {
            render_aggregate_typedef(&mut out, g, &m.inputs, &layout.kind);
        }
        if let Some(g) = &layout.outputs {
            render_aggregate_typedef(&mut out, g, &m.outputs, &layout.kind);
        }
    }
    // Phase 5: optional single width `parameter`. Defaults to the
    // concrete design width the body was built at, so a default
    // instantiation elaborates byte-identically to the
    // un-parameterized module (valid by construction). `param_env`
    // is `None` for every pre-Phase-5 / default-off module, in which
    // case emission is byte-identical to before.
    if let Some(env) = &m.param_env {
        writeln!(out, "module {} #(", m.name).unwrap();
        writeln!(out, "    parameter int {} = {}", env.name, env.design_value).unwrap();
        writeln!(out, ") (").unwrap();
    } else {
        writeln!(out, "module {} (", m.name).unwrap();
    }

    let mut first = true;

    // Phase 5b: ports that were folded into a packed aggregate are
    // skipped in the flat port list and replaced by one aggregate
    // port per side. Empty sets (default-off) ⇒ no port is skipped.
    let (agg_in_ids, agg_out_ids) = aggregate_member_ids(m);

    // Port list. Control ports are visible iff the module carries
    // sequential state locally or through instantiated descendants.
    for p in m.emitted_input_ports_in(modules) {
        if agg_in_ids.contains(&p.id) {
            continue;
        }
        if !first {
            writeln!(out, ",").unwrap();
        }
        first = false;
        write!(
            out,
            "    input  logic {} {}",
            param_width_decl(m, p),
            p.name
        )
        .unwrap();
    }
    for p in &m.outputs {
        if agg_out_ids.contains(&p.id) {
            continue;
        }
        if !first {
            writeln!(out, ",").unwrap();
        }
        first = false;
        write!(
            out,
            "    output logic {} {}",
            param_width_decl(m, p),
            p.name
        )
        .unwrap();
    }
    // Phase 5b: the aggregate ports replace their folded members.
    if let Some(layout) = &m.aggregate_layout {
        if let Some(g) = &layout.inputs {
            if !first {
                writeln!(out, ",").unwrap();
            }
            first = false;
            write!(out, "    input  {} {}", g.type_name, g.port_name).unwrap();
        }
        if let Some(g) = &layout.outputs {
            if !first {
                writeln!(out, ",").unwrap();
            }
            // No `first = false` here: the output aggregate port is
            // always the last port, so nothing reads `first` after it
            // (a trailing assignment would be a dead store).
            write!(out, "    output {} {}", g.type_name, g.port_name).unwrap();
        }
    }
    writeln!(out).unwrap();
    writeln!(out, ");").unwrap();
    writeln!(out).unwrap();

    // Phase 5b: boundary alias declarations. Input fields alias to
    // same-named `wire`s so every existing body reference resolves
    // unchanged; grouped outputs become internal `logic` (the body's
    // `assign <name> = …;` is byte-identical) and are driven onto the
    // aggregate at the end. Emitted only when a layout is present.
    if let Some(layout) = &m.aggregate_layout {
        if let Some(g) = &layout.inputs {
            for (idx, (field, pid)) in g.fields.iter().enumerate() {
                let port = m.inputs.iter().find(|p| p.id == *pid).unwrap();
                writeln!(
                    out,
                    "    wire {} {} = {}{};",
                    width_decl(port.width),
                    port.name,
                    g.port_name,
                    aggregate_field_accessor(&layout.kind, idx, field)
                )
                .unwrap();
            }
        }
        if let Some(g) = &layout.outputs {
            for (_field, pid) in &g.fields {
                let port = m.outputs.iter().find(|p| p.id == *pid).unwrap();
                writeln!(out, "    logic {} {};", width_decl(port.width), port.name).unwrap();
            }
        }
        writeln!(out).unwrap();
    }

    // Flop register declarations.
    for flop in &m.flops {
        writeln!(
            out,
            "    logic {} {};",
            param_width_decl_w(m, flop.width),
            flop_name(flop.id)
        )
        .unwrap();
    }
    if m.has_local_flops() {
        writeln!(out).unwrap();
    }

    // Phase 6 (PHASE-6-ADVANCED-MOTIFS.2.1a): inferrable-memory
    // declarations — a packed-data unpacked-depth array + its
    // registered read-data signal. Empty `m.memories` (default)
    // emits nothing → byte-identical.
    for mem in &m.memories {
        let depth = 1u128 << mem.addr_width;
        writeln!(
            out,
            "    logic {} mem_{} [0:{}];",
            width_decl(mem.data_width),
            mem.id,
            depth - 1
        )
        .unwrap();
        writeln!(
            out,
            "    logic {} {};",
            width_decl(mem.data_width),
            memrd_name(mem.id)
        )
        .unwrap();
    }
    if !m.memories.is_empty() {
        writeln!(out).unwrap();
    }

    // Phase 6 (PHASE-6-ADVANCED-MOTIFS.3.2a): generated-encoding FSM
    // declarations — the encoded-state register, its next-state
    // combinational signal, and the registered Moore output. Empty
    // `m.fsms` (default) emits nothing → byte-identical.
    for fsm in &m.fsms {
        let sw = fsm.encoding.state_width(fsm.num_states);
        writeln!(
            out,
            "    logic {} {};",
            width_decl(sw),
            fsm_state_name(fsm.id)
        )
        .unwrap();
        writeln!(
            out,
            "    logic {} {};",
            width_decl(sw),
            fsm_next_name(fsm.id)
        )
        .unwrap();
        writeln!(
            out,
            "    logic {} {};",
            width_decl(fsm.out_width),
            fsm_out_name(fsm.id)
        )
        .unwrap();
    }
    if !m.fsms.is_empty() {
        writeln!(out).unwrap();
    }

    // Internal signal declarations for every Gate node and every
    // instance-output node. Most gates are driven by continuous assigns
    // (`wire`); structured case/casez/for-fold gates are declared as
    // `logic` because their dynamic form is procedural. A structured
    // gate whose controlling inputs are already constant is still
    // declared `logic` but emitted as a continuous assignment below.
    // Instance outputs are plain wires driven by child-module port
    // connections.
    for (idx, node) in m.nodes.iter().enumerate() {
        match node {
            Node::Gate { op, width, .. } => {
                let decl_kind = if matches!(
                    op,
                    GateOp::CaseMux | GateOp::CasezMux | GateOp::ForFold { .. }
                ) {
                    "logic"
                } else {
                    "wire"
                };
                writeln!(
                    out,
                    "    {} {} {};",
                    decl_kind,
                    param_width_decl_w(m, *width),
                    names[idx].as_ref().expect("gate name assigned")
                )
                .unwrap();
                // `SV-VERSION-TARGETING.3b.2` — internal IEEE-1800-2023
                // `union soft` overlay variable for a marked low-bits slice
                // (the narrow member `n` aliases the low bits of the wide
                // member `w`, so it equals `src[width-1:0]`).
                if let Some((_, src_width)) = soft_union_slice_overlay(m, idx, sv_version) {
                    writeln!(
                        out,
                        "    union soft {{ logic {} w; logic {} n; }} {}__u;",
                        param_width_decl_w(m, src_width),
                        param_width_decl_w(m, *width),
                        names[idx].as_ref().expect("gate name assigned"),
                    )
                    .unwrap();
                }
            }
            Node::InstanceOutput { width, .. } => {
                writeln!(
                    out,
                    "    wire {} {};",
                    param_width_decl_w(m, *width),
                    names[idx]
                        .as_ref()
                        .expect("instance-output name assigned by build_names")
                )
                .unwrap();
            }
            _ => {}
        }
    }
    writeln!(out).unwrap();

    // `STRUCTURED-EMISSION-EXPANSION.2b.1` — combinational `function
    // automatic` declarations for gates marked by the
    // `crate::ir::function_emit` pass (the function-emit projection,
    // decision `0012`). A marked gate's value is computed by a `<wire>__f`
    // function over its direct operands; the gate's continuous assign
    // below becomes a call. Default-off: `function_emit_gates` empty ⇒
    // nothing emitted ⇒ byte-identical. Behaviour-preserving by
    // construction (the function returns exactly the gate's value).
    let mut emitted_function = false;
    for (idx, _node) in m.nodes.iter().enumerate() {
        if let Some((op, operands, width)) = function_emit_gate(m, idx) {
            let name = names[idx].as_ref().expect("gate name assigned");
            out.push_str(&render_gate_function_decl(m, name, op, operands, width));
            emitted_function = true;
        }
    }
    if emitted_function {
        writeln!(out).unwrap();
    }

    // `STRUCTURED-EMISSION-EXPANSION.4b` — single-level `generate for` loops
    // for `{N{x}}` replication gates marked by the `crate::ir::generate_loop`
    // pass (the generate-loop projection, decision `0013`). A marked gate's
    // value is produced by a `genvar` + `generate for` over the replication
    // count; the gate's continuous assign below is suppressed. Default-off:
    // `generate_loop_gates` empty ⇒ nothing emitted ⇒ byte-identical. The
    // unrolled loop is byte-equivalent to the inline `{N{x}}`, so this is
    // behaviour-preserving by construction.
    let mut emitted_generate_loop = false;
    for (idx, _node) in m.nodes.iter().enumerate() {
        if let Some((lane, n)) = generate_loop_gate(m, idx) {
            let name = names[idx].as_ref().expect("gate name assigned");
            out.push_str(&render_generate_loop_block(name, lane, n, m, &names));
            emitted_generate_loop = true;
        }
    }
    if emitted_generate_loop {
        writeln!(out).unwrap();
    }

    // Combinational assigns for every gate. Fully static structured
    // gates are lowered here too; keeping them out of `always_comb`
    // avoids empty-sensitivity warnings in strict downstream tools.
    for (idx, node) in m.nodes.iter().enumerate() {
        if let Node::Gate {
            op,
            operands,
            width,
            ..
        } = node
        {
            // `STRUCTURED-EMISSION-EXPANSION.4b` — a `{N{x}}` replication
            // marked for the `generate for` loop projection is driven by the
            // generate block emitted above instead of the inline
            // `assign <wire> = {N{x}};`. Checked first (the generate-loop and
            // function-emit / soft_union projections are disjoint by the
            // annotation rule). Default-off ⇒ never taken.
            if generate_loop_gate(m, idx).is_some() {
                continue;
            }
            if matches!(
                op,
                GateOp::CaseMux | GateOp::CasezMux | GateOp::ForFold { .. }
            ) {
                if let Some(rhs) = render_static_structured_gate(*op, operands, *width, m, &names) {
                    writeln!(
                        out,
                        "    assign {} = {};",
                        names[idx].as_ref().unwrap(),
                        rhs
                    )
                    .unwrap();
                }
                continue;
            }
            // `SV-VERSION-TARGETING.3b.2` — a marked low-bits slice on a
            // 2023 target is driven through the internal `union soft` overlay
            // (`u.w = src; gate = u.n`) instead of `src[hi:0]`. Below 2023 the
            // overlay is `None`, so this down-gates to the plain slice.
            if let Some((src, _)) = soft_union_slice_overlay(m, idx, sv_version) {
                let name = names[idx].as_ref().unwrap();
                let src_ref = node_ref(src, m, &names);
                writeln!(out, "    assign {name}__u.w = {src_ref};").unwrap();
                writeln!(out, "    assign {name} = {name}__u.n;").unwrap();
                continue;
            }
            // `STRUCTURED-EMISSION-EXPANSION.2b.1` — a gate marked for
            // function emission is driven by a call to its `<wire>__f`
            // `function automatic` (declared above) instead of the inline
            // operation. Mutually exclusive with the soft_union overlay (a
            // `union soft` slice is never a function-emit candidate).
            // Default-off ⇒ never taken.
            if function_emit_gate(m, idx).is_some() {
                let name = names[idx].as_ref().unwrap();
                let call = render_gate_function_call(name, operands, m, &names);
                writeln!(out, "    assign {name} = {call};").unwrap();
                continue;
            }
            let rhs = render_gate(*op, operands, m, &names);
            writeln!(
                out,
                "    assign {} = {};",
                names[idx].as_ref().unwrap(),
                rhs
            )
            .unwrap();
        }
    }
    if !m.nodes.is_empty() {
        writeln!(out).unwrap();
    }

    // Procedural combinational blocks for structured case/casez/for-fold muxes.
    for (idx, node) in m.nodes.iter().enumerate() {
        let Node::Gate {
            op,
            operands,
            width,
            ..
        } = node
        else {
            continue;
        };
        if !matches!(
            op,
            GateOp::CaseMux | GateOp::CasezMux | GateOp::ForFold { .. }
        ) {
            continue;
        }
        let name = names[idx].as_ref().expect("gate name assigned");
        if render_static_structured_gate(*op, operands, *width, m, &names).is_some() {
            continue;
        }
        writeln!(out, "    always_comb begin").unwrap();
        match op {
            GateOp::CaseMux => {
                let sel = node_ref(operands[0], m, &names);
                let sel_width = m.nodes[operands[0] as usize].width();
                writeln!(out, "        case ({sel})").unwrap();
                for (arm_idx, data_id) in operands[1..].iter().enumerate() {
                    let data = node_ref(*data_id, m, &names);
                    writeln!(
                        out,
                        "            {}'d{}: {} = {};",
                        sel_width, arm_idx, name, data
                    )
                    .unwrap();
                }
            }
            GateOp::CasezMux => {
                let sel = node_ref(operands[0], m, &names);
                writeln!(out, "        casez ({sel})").unwrap();
                for arm in operands[1..].chunks_exact(3) {
                    let pattern = render_casez_pattern(arm[0], arm[1], m);
                    let data = node_ref(arm[2], m, &names);
                    writeln!(out, "            {pattern}: {name} = {data};").unwrap();
                }
            }
            GateOp::ForFold {
                kind,
                trip_count,
                chunk_width,
            } => {
                let src_node = &m.nodes[operands[0] as usize];
                let src = node_ref(operands[0], m, &names);
                let init = for_fold_init_literal(kind, *chunk_width);
                let symbol = for_fold_symbol(kind);
                let src_ref = if matches!(src_node, Node::Constant { .. }) {
                    let tmp = format!("for_fold_src_{idx}");
                    writeln!(
                        out,
                        "        logic {} {};",
                        width_decl(src_node.width()),
                        tmp
                    )
                    .unwrap();
                    writeln!(out, "        {tmp} = {src};").unwrap();
                    tmp
                } else {
                    src
                };
                writeln!(out, "        {name} = {init};").unwrap();
                writeln!(out, "        for (int i = 0; i < {trip_count}; i++) begin").unwrap();
                writeln!(
                    out,
                    "            {name} = {name} {symbol} {src_ref}[(i * {chunk_width}) +: {chunk_width}];"
                )
                .unwrap();
                writeln!(out, "        end").unwrap();
            }
            _ => unreachable!("non-procedural gate filtered above"),
        }
        if matches!(op, GateOp::CaseMux | GateOp::CasezMux) {
            writeln!(out, "            default: {} = {}'h0;", name, width).unwrap();
            writeln!(out, "        endcase").unwrap();
        }
        writeln!(out, "    end").unwrap();
        writeln!(out).unwrap();
    }

    // Child-module instances.
    if !m.instances.is_empty() {
        let modules = modules.expect("hierarchical emission requires design context");
        for instance in &m.instances {
            let child = modules
                .get(instance.module.as_str())
                .expect("child module exists in design");
            // Phase 5: when this instance carries parameter overrides
            // (`PHASE-5-PARAMETERIZATION.2.2.3b`), emit a
            // `#(.NAME(value), …)` override list. Empty bindings (every
            // pre-Phase-5 / default-off instance) emit no `#(...)`, so
            // emission is byte-identical to before.
            if instance.param_bindings.is_empty() {
                writeln!(out, "    {} {} (", child.name, instance.name).unwrap();
            } else {
                let overrides = instance
                    .param_bindings
                    .iter()
                    .map(|(name, value)| format!(".{name}({value})"))
                    .collect::<Vec<_>>()
                    .join(", ");
                writeln!(
                    out,
                    "    {} #({}) {} (",
                    child.name, overrides, instance.name
                )
                .unwrap();
            }
            let input_bindings: BTreeMap<_, _> = instance.inputs.iter().copied().collect();

            let mut connections = Vec::new();
            for input in child.emitted_input_ports_in(Some(modules)) {
                let node_id = input_bindings
                    .get(&input.id)
                    .copied()
                    .expect("child input is bound by validated design");
                connections.push(format!(
                    "        .{}({})",
                    input.name,
                    node_ref(node_id, m, &names)
                ));
            }
            for output in &child.outputs {
                if let Some(&node_id) = instance_outputs.get(&(instance.id, output.id)) {
                    connections.push(format!(
                        "        .{}({})",
                        output.name,
                        node_ref(node_id, m, &names)
                    ));
                } else {
                    connections.push(format!("        .{}()", output.name));
                }
            }

            writeln!(out, "{}", connections.join(",\n")).unwrap();
            writeln!(out, "    );").unwrap();
            writeln!(out).unwrap();
        }
    }

    // Sequential always_ff blocks. Per `MULTI-CLOCK-CDC.2`, one
    // `always_ff @(posedge clk_X or negedge rst_n_X) begin` per
    // declared clock domain (synthesised single-domain default
    // when `Module.clock_domains` is empty — the K=1 path is
    // byte-identical to pre-`.2` ANVIL). Flops are grouped by
    // `Module::flop_domain(id)`; the K=1 default leaves
    // `Module.flop_domains` empty and every flop lands in domain
    // 0, so the K=1 output is byte-identical (verified by
    // `tests/snapshots.rs` + `tests/book_examples.rs`).
    if m.has_local_flops() {
        let domains = m.effective_clock_domains();
        for (domain_idx, domain) in domains.iter().enumerate() {
            let clk = port_name(m, domain.clk);
            let rst = port_name(m, domain.rst_n);
            let flops_in_domain: Vec<&Flop> = m
                .flops
                .iter()
                .filter(|flop| m.flop_domain(flop.id) as usize == domain_idx)
                .collect();
            if flops_in_domain.is_empty() {
                continue;
            }
            writeln!(
                out,
                "    always_ff @(posedge {} or negedge {}) begin",
                clk, rst
            )
            .unwrap();
            writeln!(out, "        if (!{}) begin", rst).unwrap();
            for flop in &flops_in_domain {
                writeln!(
                    out,
                    "            {} <= {}'h{:x};",
                    flop_name(flop.id),
                    flop.width,
                    flop.reset_val
                )
                .unwrap();
            }
            writeln!(out, "        end else begin").unwrap();
            for flop in &flops_in_domain {
                let d_id = flop.d.expect("flop D must be set before emission");
                let d_ref = node_ref(d_id, m, &names);
                writeln!(out, "            {} <= {};", flop_name(flop.id), d_ref).unwrap();
            }
            writeln!(out, "        end").unwrap();
            writeln!(out, "    end").unwrap();
            writeln!(out).unwrap();
        }
    }

    // Phase 6: inferrable-memory synchronous write/read blocks. One
    // `always_ff @(posedge clk)` per memory (no reset — the LRM /
    // Yosys $mem_v2-inferred template from PHASE-6-ADVANCED-MOTIFS.1).
    if !m.memories.is_empty() {
        let clk = port_name(m, m.clock.expect("memory requires a clock"));
        for mem in &m.memories {
            let we = node_ref(mem.we, m, &names);
            let waddr = node_ref(mem.waddr, m, &names);
            let wdata = node_ref(mem.wdata, m, &names);
            let raddr = node_ref(mem.raddr, m, &names);
            writeln!(out, "    always_ff @(posedge {}) begin", clk).unwrap();
            writeln!(
                out,
                "        if ({}) mem_{}[{}] <= {};",
                we, mem.id, waddr, wdata
            )
            .unwrap();
            writeln!(
                out,
                "        {} <= mem_{}[{}];",
                memrd_name(mem.id),
                mem.id,
                raddr
            )
            .unwrap();
            writeln!(out, "    end").unwrap();
            writeln!(out).unwrap();
        }
    }

    // Phase 6 (PHASE-6-ADVANCED-MOTIFS.3.2a): generated-encoding Moore
    // FSM blocks — the `.3.1`-probed-clean template (encoding-derived
    // `localparam` state constants + an async-low-reset state register
    // + an `always_comb` next-state `case` selected by `sel` + an
    // `always_comb` Moore output `case`). Empty `m.fsms` → nothing.
    if !m.fsms.is_empty() {
        let clk = port_name(m, m.clock.expect("fsm requires a clock"));
        let rst = port_name(m, m.reset.expect("fsm requires a reset"));
        for fsm in &m.fsms {
            let sw = fsm.encoding.state_width(fsm.num_states);
            let st = fsm_state_name(fsm.id);
            let nx = fsm_next_name(fsm.id);
            let outw = fsm_out_name(fsm.id);
            let sel = node_ref(fsm.sel, m, &names);
            let sname = |s: u32| format!("FSM{}_S{}", fsm.id, s);

            // Encoding-derived state constants.
            for s in 0..fsm.num_states {
                writeln!(
                    out,
                    "    localparam logic {} {} = {};",
                    width_decl(sw),
                    sname(s),
                    fsm_state_lit(fsm.encoding, fsm.num_states, s)
                )
                .unwrap();
            }

            // Next-state decode.
            writeln!(out, "    always_comb begin").unwrap();
            writeln!(out, "        {} = {};", nx, st).unwrap();
            writeln!(out, "        case ({})", st).unwrap();
            for s in 0..fsm.num_states {
                writeln!(out, "            {}: case ({})", sname(s), sel).unwrap();
                for (sv, &ns) in fsm.transitions[s as usize].iter().enumerate() {
                    writeln!(
                        out,
                        "                {}'h{:x}: {} = {};",
                        fsm.sel_width,
                        sv,
                        nx,
                        sname(ns)
                    )
                    .unwrap();
                }
                writeln!(out, "                default: {} = {};", nx, sname(0)).unwrap();
                writeln!(out, "            endcase").unwrap();
            }
            writeln!(out, "            default: {} = {};", nx, sname(0)).unwrap();
            writeln!(out, "        endcase").unwrap();
            writeln!(out, "    end").unwrap();

            // State register (async-low reset to state 0).
            writeln!(
                out,
                "    always_ff @(posedge {} or negedge {}) begin",
                clk, rst
            )
            .unwrap();
            writeln!(out, "        if (!{}) {} <= {};", rst, st, sname(0)).unwrap();
            writeln!(out, "        else {} <= {};", st, nx).unwrap();
            writeln!(out, "    end").unwrap();

            // Moore output decode.
            writeln!(out, "    always_comb begin").unwrap();
            writeln!(out, "        case ({})", st).unwrap();
            for s in 0..fsm.num_states {
                writeln!(
                    out,
                    "            {}: {} = {}'h{:x};",
                    sname(s),
                    outw,
                    fsm.out_width,
                    fsm.outputs[s as usize]
                )
                .unwrap();
            }
            writeln!(out, "            default: {} = {}'h0;", outw, fsm.out_width).unwrap();
            writeln!(out, "        endcase").unwrap();
            writeln!(out, "    end").unwrap();
            writeln!(out).unwrap();
        }
    }

    // Output port assigns.
    for (port_id, node_id) in &m.drives {
        let out_port = m.outputs.iter().find(|p| p.id == *port_id).unwrap();
        let rhs = node_ref(*node_id, m, &names);
        writeln!(out, "    assign {} = {};", out_port.name, rhs).unwrap();
    }

    // Phase 5b: drive each grouped output's internal `logic` onto the
    // aggregate port field at the boundary. No-op when default-off.
    if let Some(layout) = m.aggregate_layout.as_ref() {
        if let Some(g) = layout.outputs.as_ref() {
            for (idx, (field, pid)) in g.fields.iter().enumerate() {
                let port = m.outputs.iter().find(|p| p.id == *pid).unwrap();
                writeln!(
                    out,
                    "    assign {}{} = {};",
                    g.port_name,
                    aggregate_field_accessor(&layout.kind, idx, field),
                    port.name
                )
                .unwrap();
            }
        }
    }

    writeln!(out, "endmodule").unwrap();
    out
}

/// Deterministic identifier for a `GateOp` — the per-kind prefix used
/// in emitted wire names (`<kind>_<counter>`). Kinds are lowercase
/// snake_case. Suffixed with a trailing `_` in callers.
fn gate_kind_name(op: GateOp) -> &'static str {
    use GateOp::*;
    match op {
        And => "and",
        Or => "or",
        Xor => "xor",
        Not => "not",
        Add => "add",
        Sub => "sub",
        Mul => "mul",
        Eq => "eq",
        Neq => "neq",
        Lt => "lt",
        Gt => "gt",
        Le => "le",
        Ge => "ge",
        Mux => "mux",
        CaseMux => "case_mux",
        CasezMux => "casez_mux",
        ForFold { kind, .. } => match kind {
            ForFoldKind::Xor => "for_fold_xor",
            ForFoldKind::Or => "for_fold_or",
            ForFoldKind::And => "for_fold_and",
            ForFoldKind::Add => "for_fold_add",
        },
        Slice { .. } => "slice",
        Concat => "concat",
        RedAnd => "red_and",
        RedOr => "red_or",
        RedXor => "red_xor",
        Shl => "shl",
        Shr => "shr",
    }
}

