/// Walk the module once, assigning per-kind counters to every gate.
/// Non-gate nodes (`PrimaryInput`, `Constant`, `FlopQ`) get `None`
/// because their reference form is derived from elsewhere (port name,
/// literal, flop id).
fn build_names(m: &Module) -> Vec<Option<String>> {
    use std::collections::BTreeMap;
    // BTreeMap for deterministic iteration order when we log the
    // per-kind counts below. HashMap would work for correctness but
    // would surface non-determinism in trace output.
    let mut counters: BTreeMap<&'static str, u32> = BTreeMap::new();
    let mut names: Vec<Option<String>> = Vec::with_capacity(m.nodes.len());
    for node in &m.nodes {
        let name = match node {
            Node::Gate { op, .. } => {
                let kind = gate_kind_name(*op);
                let counter = counters.entry(kind).or_insert(0);
                let n = *counter;
                *counter += 1;
                Some(format!("{}_{}", kind, n))
            }
            Node::InstanceOutput { instance, port, .. } => {
                Some(instance_output_name(*instance, *port))
            }
            _ => None,
        };
        names.push(name);
    }
    debug!(
        per_kind = ?counters,
        "build_names: per-kind counters assigned"
    );
    names
}

fn width_decl(w: u32) -> String {
    if w == 1 {
        String::new()
    } else {
        format!("[{}:0]", w - 1)
    }
}

/// Phase 5b: emit the typedef for one aggregate group. Field widths are
/// looked up from `ports` (the module's `inputs` or `outputs`); field
/// order is the group's emitted port order (the bit layout is internal
/// to the projection).
///
/// `StructPacked` renders `typedef struct packed { … } <type>;` over
/// the per-field widths. `ArrayPacked` (AGGREGATE-ARRAY-PACKING) renders
/// `typedef logic [N-1:0]<elem-width> <type>;` — a packed array over a
/// uniform-width group (guaranteed by the annotate pass), bit-equivalent
/// to the field concatenation; element access is positional.
fn render_aggregate_typedef(
    out: &mut String,
    g: &AggregateGroup,
    ports: &[Port],
    kind: &AggregateKind,
) {
    let width_of = |pid: PortId| -> u32 {
        ports
            .iter()
            .find(|p| p.id == pid)
            .map(|p| p.width)
            .unwrap_or(1)
    };
    match kind {
        AggregateKind::StructPacked => {
            writeln!(out, "typedef struct packed {{").unwrap();
            for (field, pid) in &g.fields {
                writeln!(out, "    logic {} {};", width_decl(width_of(*pid)), field).unwrap();
            }
            writeln!(out, "}} {};", g.type_name).unwrap();
        }
        AggregateKind::ArrayPacked => {
            let n = g.fields.len();
            let w = g.fields.first().map(|(_, pid)| width_of(*pid)).unwrap_or(1);
            debug_assert!(
                g.fields.iter().all(|(_, pid)| width_of(*pid) == w),
                "ArrayPacked requires a uniform-width group"
            );
            // `[N-1:0]` outer dim; `width_decl(w)` is `[W-1:0]` (w>1) or
            // empty (w==1, a 1-bit element).
            writeln!(
                out,
                "typedef logic [{}:0]{} {};",
                n - 1,
                width_decl(w),
                g.type_name
            )
            .unwrap();
        }
    }
    writeln!(out).unwrap();
}

/// Boundary accessor for the `idx`-th field (named `field`) of an
/// aggregate port of the given `kind`: `.field` for a packed struct,
/// `[idx]` for a packed array.
fn aggregate_field_accessor(kind: &AggregateKind, idx: usize, field: &str) -> String {
    match kind {
        AggregateKind::StructPacked => format!(".{field}"),
        AggregateKind::ArrayPacked => format!("[{idx}]"),
    }
}

/// Phase 5b: the `(input_member_ids, output_member_ids)` port ids that
/// were folded into a packed aggregate, so the flat port list can skip
/// them. Both empty when `aggregate_layout == None` (default-off).
fn aggregate_member_ids(m: &Module) -> (BTreeSet<PortId>, BTreeSet<PortId>) {
    let collect = |grp: Option<&AggregateGroup>| -> BTreeSet<PortId> {
        grp.map(|g| g.fields.iter().map(|(_, pid)| *pid).collect())
            .unwrap_or_default()
    };
    match &m.aggregate_layout {
        Some(l) => (collect(l.inputs.as_ref()), collect(l.outputs.as_ref())),
        None => (BTreeSet::new(), BTreeSet::new()),
    }
}

/// Width declaration for an interface `port`, honoring Phase 5
/// parameterization: if the module carries a `ParamEnv` and this
/// port id is in the parameterized set, the width renders
/// symbolically as `[<param>-1:0]`; otherwise it falls back to the
/// concrete `width_decl`. Modules with `param_env == None` (every
/// default-off / pre-Phase-5 module) always take the concrete path,
/// so emission is byte-identical to before.
fn param_width_decl(m: &Module, port: &Port) -> String {
    if let Some(env) = &m.param_env {
        let parameterized = match port.dir {
            Direction::In => m.parameterized_input_ports.contains(&port.id),
            Direction::Out => m.parameterized_output_ports.contains(&port.id),
        };
        if parameterized {
            return format!("[{}-1:0]", env.name);
        }
    }
    width_decl(port.width)
}

/// Width declaration for an internal width-bearing site (gate wire,
/// instance-output wire, flop register) honoring Phase 5
/// parameterization. A parameterized module is **width-homogeneous**
/// by the `crate::ir::param` soundness gate, so every such width
/// equals the parameter's design value and renders symbolically as
/// `[<param>-1:0]`, making the whole emitted body width-generic. Any
/// width that is not the design value (and every site in a
/// non-parameterized / `param_env == None` module) falls back to the
/// concrete `width_decl`, so default-off emission is byte-identical.
fn param_width_decl_w(m: &Module, w: u32) -> String {
    if let Some(env) = &m.param_env {
        if w == env.design_value {
            return format!("[{}-1:0]", env.name);
        }
    }
    width_decl(w)
}

fn port_name(m: &Module, id: crate::ir::PortId) -> String {
    m.inputs
        .iter()
        .chain(m.outputs.iter())
        .find(|p| p.id == id)
        .map(|p| p.name.clone())
        .unwrap_or_else(|| format!("port_{}", id))
}

fn node_ref(id: NodeId, m: &Module, names: &[Option<String>]) -> String {
    match &m.nodes[id as usize] {
        Node::PrimaryInput { port, .. } => m
            .inputs
            .iter()
            .find(|p| p.id == *port)
            .unwrap()
            .name
            .clone(),
        Node::Constant { width, value } => const_literal(*width, *value),
        Node::FlopQ { flop, .. } => flop_name(*flop),
        Node::MemRead { mem, .. } => memrd_name(*mem),
        Node::FsmOut { fsm, .. } => fsm_out_name(*fsm),
        Node::InstanceOutput { .. } => names[id as usize]
            .clone()
            .expect("instance-output name assigned by build_names"),
        Node::Gate { .. } => names[id as usize]
            .clone()
            .expect("gate name assigned by build_names"),
    }
}

/// `SV-VERSION-TARGETING.3b.2` — when `sv_version` permits IEEE 1800-2023 and
/// the gate at `idx` was marked by the `crate::ir::soft_union` pass as a proper
/// low-bits slice, return `(source NodeId, source bit-width)` so the caller
/// renders the gate via an internal heterogeneous-width `union soft` overlay
/// (`u.w = src; gate = u.n`) instead of a plain `src[hi:0]` bit-select.
/// `None` ⇒ render the plain slice — the down-gated path below 2023, or an
/// unmarked / non-qualifying gate. The qualifying contract is re-checked
/// defensively so a stale marker can never produce an invalid overlay.
fn soft_union_slice_overlay(
    m: &Module,
    idx: usize,
    sv_version: SvVersion,
) -> Option<(NodeId, u32)> {
    if !sv_version.permits(SvVersion::Sv2023) {
        return None;
    }
    if !m.soft_union_slice_gates.contains(&(idx as NodeId)) {
        return None;
    }
    let Node::Gate {
        op: GateOp::Slice { hi, lo },
        operands,
        width,
        ..
    } = &m.nodes[idx]
    else {
        return None;
    };
    if *lo != 0 {
        return None;
    }
    let src = *operands.first()?;
    let src_node = m.nodes.get(src as usize)?;
    if matches!(src_node, Node::Constant { .. }) {
        return None;
    }
    let src_width = src_node.width();
    // Real narrowing into a strictly-narrower member (the 2023 *soft*
    // requirement: members `w` and `n` have different widths).
    if *width >= 1 && *hi + 1 == *width && *width < src_width {
        Some((src, src_width))
    } else {
        None
    }
}

fn render_gate(op: GateOp, operands: &[NodeId], m: &Module, names: &[Option<String>]) -> String {
    use GateOp::*;
    let a = |i: usize| node_ref(operands[i], m, names);
    let joined = |sep: &str| -> String {
        operands
            .iter()
            .map(|id| node_ref(*id, m, names))
            .collect::<Vec<_>>()
            .join(sep)
    };
    match op {
        // N-arity associative ops: join operand refs with the infix symbol.
        // For N = 2 this recovers the classic binary form.
        And => joined(" & "),
        Or => joined(" | "),
        Xor => joined(" ^ "),
        Add => joined(" + "),
        Mul => joined(" * "),
        // Sub is strictly 2-arity (not associative).
        Sub => format!("{} - {}", a(0), a(1)),
        Not => format!("~{}", a(0)),
        Eq => format!("{} == {}", a(0), a(1)),
        Neq => format!("{} != {}", a(0), a(1)),
        Lt => format!("{} < {}", a(0), a(1)),
        Gt => format!("{} > {}", a(0), a(1)),
        Le => format!("{} <= {}", a(0), a(1)),
        Ge => format!("{} >= {}", a(0), a(1)),
        Mux => format!("({}) ? ({}) : ({})", a(0), a(1), a(2)),
        CaseMux | CasezMux | ForFold { .. } => {
            unreachable!("procedural structured blocks are emitted via always_comb")
        }
        Slice { hi, lo } => {
            if let Node::Constant { value, .. } = m.nodes[operands[0] as usize] {
                let slice_width = hi - lo + 1;
                let sliced = (value >> lo) & bitmask(slice_width);
                return const_literal(slice_width, sliced);
            }
            let src = a(0);
            let src_width = m.nodes[operands[0] as usize].width();
            if src_width == 1 && hi == 0 && lo == 0 {
                src
            } else if hi == lo {
                format!("{src}[{hi}]")
            } else {
                format!("{src}[{hi}:{lo}]")
            }
        }
        Concat => {
            // If every operand points at the same NodeId, emit the
            // canonical SystemVerilog replication form `{N{expr}}`
            // instead of a flat list `{expr, expr, ..., expr}`. This
            // is the idiom for broadcasting a 1-bit select across a
            // W-bit mask (`{W{sel_i}} & data_i` in one-hot muxes).
            // Synthesis-equivalent; purely a readability fix.
            if operands.len() >= 2 && operands.iter().all(|id| *id == operands[0]) {
                return format!("{{{}{{{}}}}}", operands.len(), a(0));
            }
            let parts: Vec<_> = operands.iter().map(|id| node_ref(*id, m, names)).collect();
            format!("{{{}}}", parts.join(", "))
        }
        RedAnd => format!("&({})", a(0)),
        RedOr => format!("|({})", a(0)),
        RedXor => format!("^({})", a(0)),
        Shl => format!("{} << {}", a(0), a(1)),
        Shr => format!("{} >> {}", a(0), a(1)),
    }
}

/// `STRUCTURED-EMISSION-EXPANSION.2b.1` — if the gate at `idx` is marked
/// for combinational `function automatic` emission and still qualifies (a
/// non-structured gate with at least one operand), return
/// `(op, operands, width)` so the caller renders a `function automatic`
/// projection (`<wire>__f`) + a call instead of the inline `assign`.
/// `None` ⇒ render the gate inline — an unmarked / non-qualifying gate, or
/// the default-off path where `function_emit_gates` is empty. The
/// qualifying contract is re-checked defensively so a stale marker can
/// never produce an invalid function (mirrors `soft_union_slice_overlay`).
fn function_emit_gate(m: &Module, idx: usize) -> Option<(GateOp, &[NodeId], u32)> {
    if !m.function_emit_gates.contains(&(idx as NodeId)) {
        return None;
    }
    let Node::Gate {
        op,
        operands,
        width,
        ..
    } = &m.nodes[idx]
    else {
        return None;
    };
    // Structured (procedural) blocks and `Slice` bit-selects are excluded
    // from function-emit candidacy (see `crate::ir::function_emit`): the
    // former are emitted via `always_comb`, the latter uses only a
    // sub-range of its operand (a full-width param would trip
    // `UNUSEDSIGNAL`). Re-checked here defensively.
    if matches!(
        op,
        GateOp::CaseMux | GateOp::CasezMux | GateOp::ForFold { .. } | GateOp::Slice { .. }
    ) {
        return None;
    }
    if operands.is_empty() {
        return None;
    }
    Some((*op, operands.as_slice(), *width))
}

/// The body expression of a combinational `function automatic` projection
/// of gate `op` over positional parameters `a0..a{n-1}`. The
/// behaviour-preserving counterpart of [`render_gate`]: each operand
/// reference is the positional parameter name `a{i}` rather than the
/// operand's module-level [`node_ref`], so the function returns exactly
/// the gate's value. `operand_widths[i]` is operand `i`'s bit-width
/// (needed only for the `Slice` 1-bit special case, mirroring
/// `render_gate`). Structured gates and `Slice` never reach here — both
/// are excluded from function-emit candidacy in the first cut; the
/// (correct) `Slice` arm is retained for a future slice-aware projection
/// that would pass only the used sub-range.
fn render_gate_function_body(op: GateOp, operand_widths: &[u32]) -> String {
    use GateOp::*;
    let p = |i: usize| format!("a{i}");
    let joined = |sep: &str| -> String {
        (0..operand_widths.len())
            .map(p)
            .collect::<Vec<_>>()
            .join(sep)
    };
    match op {
        And => joined(" & "),
        Or => joined(" | "),
        Xor => joined(" ^ "),
        Add => joined(" + "),
        Mul => joined(" * "),
        Sub => format!("{} - {}", p(0), p(1)),
        Not => format!("~{}", p(0)),
        Eq => format!("{} == {}", p(0), p(1)),
        Neq => format!("{} != {}", p(0), p(1)),
        Lt => format!("{} < {}", p(0), p(1)),
        Gt => format!("{} > {}", p(0), p(1)),
        Le => format!("{} <= {}", p(0), p(1)),
        Ge => format!("{} >= {}", p(0), p(1)),
        Mux => format!("({}) ? ({}) : ({})", p(0), p(1), p(2)),
        Slice { hi, lo } => {
            let src_width = operand_widths[0];
            if src_width == 1 && hi == 0 && lo == 0 {
                p(0)
            } else if hi == lo {
                format!("{}[{hi}]", p(0))
            } else {
                format!("{}[{hi}:{lo}]", p(0))
            }
        }
        Concat => {
            let parts: Vec<String> = (0..operand_widths.len()).map(p).collect();
            format!("{{{}}}", parts.join(", "))
        }
        RedAnd => format!("&({})", p(0)),
        RedOr => format!("|({})", p(0)),
        RedXor => format!("^({})", p(0)),
        Shl => format!("{} << {}", p(0), p(1)),
        Shr => format!("{} >> {}", p(0), p(1)),
        CaseMux | CasezMux | ForFold { .. } => {
            unreachable!("structured gates are excluded from function-emit candidacy")
        }
    }
}

/// `STRUCTURED-EMISSION-EXPANSION.2b.1` — render the `function automatic`
/// declaration for a function-emitted gate `name` (`<name>__f`): the
/// return type is the gate width, the positional params `a0..a{n-1}`
/// carry the operand widths, and the body is the behaviour-preserving
/// re-expression of the operation over those params
/// ([`render_gate_function_body`]).
fn render_gate_function_decl(
    m: &Module,
    name: &str,
    op: GateOp,
    operands: &[NodeId],
    width: u32,
) -> String {
    let fname = format!("{name}__f");
    let operand_widths: Vec<u32> = operands
        .iter()
        .map(|id| m.nodes[*id as usize].width())
        .collect();
    let params: Vec<String> = operand_widths
        .iter()
        .enumerate()
        .map(|(i, w)| format!("input logic {} a{}", param_width_decl_w(m, *w), i))
        .collect();
    let mut s = String::new();
    writeln!(
        s,
        "    function automatic logic {} {}({});",
        param_width_decl_w(m, width),
        fname,
        params.join(", ")
    )
    .unwrap();
    writeln!(
        s,
        "        {} = {};",
        fname,
        render_gate_function_body(op, &operand_widths)
    )
    .unwrap();
    writeln!(s, "    endfunction").unwrap();
    s
}

/// `STRUCTURED-EMISSION-EXPANSION.2b.1` — the call-site expression
/// `<name>__f(<operand refs>)` that replaces a function-emitted gate's
/// inline `assign` RHS. The arguments are the gate's operands rendered
/// with the normal module-level [`node_ref`] (constants fold to literals,
/// inputs to port names, gates to wire names), in positional order — so
/// duplicate operands pass the same reference into distinct positional
/// parameters.
fn render_gate_function_call(
    name: &str,
    operands: &[NodeId],
    m: &Module,
    names: &[Option<String>],
) -> String {
    let args: Vec<String> = operands.iter().map(|id| node_ref(*id, m, names)).collect();
    format!("{name}__f({})", args.join(", "))
}

/// `STRUCTURED-EMISSION-EXPANSION.4b` — if the gate at `idx` is marked for
/// the `generate for` loop projection and still qualifies (a `{N{x}}`
/// replication `Concat`: `>= 2` operands all the same `NodeId`, a 1-bit
/// lane, and result width `== N`), return `(lane_operand, N)` so the caller
/// renders a `genvar` + `generate for` block instead of the inline
/// `assign <wire> = {N{x}};`. `None` ⇒ render the gate inline — an unmarked /
/// non-qualifying gate, or the default-off path where `generate_loop_gates`
/// is empty. The qualifying contract is re-checked defensively so a stale
/// marker can never produce an invalid loop (mirrors `function_emit_gate`).
fn generate_loop_gate(m: &Module, idx: usize) -> Option<(NodeId, usize)> {
    if !m.generate_loop_gates.contains(&(idx as NodeId)) {
        return None;
    }
    let Node::Gate {
        op: GateOp::Concat,
        operands,
        width,
        ..
    } = &m.nodes[idx]
    else {
        return None;
    };
    if operands.len() < 2 {
        return None;
    }
    let first = *operands.first()?;
    if !operands.iter().all(|o| *o == first) {
        return None;
    }
    // 1-bit lane ⇒ result width == N (each result bit is the lane), so the
    // `assign <wire>[gi] = <x>;` body is byte-faithful to `{N{x}}`.
    if m.nodes[first as usize].width() != 1 || *width as usize != operands.len() {
        return None;
    }
    Some((first, operands.len()))
}

/// `STRUCTURED-EMISSION-EXPANSION.4b` — render the single-level
/// `generate for` loop for a replication-emitted gate `name` (`{N{x}}`): a
/// `<name>__gi` genvar, a `generate for (gi = 0; gi < N; gi = gi + 1)` over
/// the replication count, and a body `assign <name>[gi] = <x>;` where `<x>`
/// is the replicated lane rendered with the normal module-level
/// [`node_ref`]. The unrolled loop is byte-equivalent to the inline
/// `{N{x}}`. `gi = gi + 1` is the maximally-portable increment form (the
/// `.3` probe also verified `gi++`).
fn render_generate_loop_block(
    name: &str,
    lane: NodeId,
    n: usize,
    m: &Module,
    names: &[Option<String>],
) -> String {
    let gi = format!("{name}__gi");
    let label = format!("{name}__gen");
    let x = node_ref(lane, m, names);
    let mut s = String::new();
    writeln!(s, "    genvar {gi};").unwrap();
    writeln!(s, "    generate").unwrap();
    writeln!(
        s,
        "        for ({gi} = 0; {gi} < {n}; {gi} = {gi} + 1) begin : {label}"
    )
    .unwrap();
    writeln!(s, "            assign {name}[{gi}] = {x};").unwrap();
    writeln!(s, "        end").unwrap();
    writeln!(s, "    endgenerate").unwrap();
    s
}

fn render_static_structured_gate(
    op: GateOp,
    operands: &[NodeId],
    width: u32,
    m: &Module,
    names: &[Option<String>],
) -> Option<String> {
    match op {
        GateOp::CaseMux => {
            let sel = constant_value(m, operands[0])?;
            Some(
                usize::try_from(sel)
                    .ok()
                    .and_then(|idx| operands[1..].get(idx))
                    .map(|arm| node_ref(*arm, m, names))
                    .unwrap_or_else(|| const_literal(width, 0)),
            )
        }
        GateOp::CasezMux => {
            let sel_width = m.nodes[operands[0] as usize].width();
            let sel = constant_value(m, operands[0])?;
            for arm in operands[1..].chunks_exact(3) {
                let pattern_value = constant_value(m, arm[0])?;
                let wildcard_mask = constant_value(m, arm[1])?;
                if casez_pattern_matches(sel, pattern_value, wildcard_mask, sel_width) {
                    return Some(node_ref(arm[2], m, names));
                }
            }
            Some(const_literal(width, 0))
        }
        GateOp::ForFold {
            kind,
            trip_count,
            chunk_width,
        } => {
            let src = constant_value(m, operands[0])?;
            let value = eval_static_for_fold(kind, trip_count, chunk_width, src);
            Some(const_literal(chunk_width, value))
        }
        _ => None,
    }
}

fn constant_value(m: &Module, id: NodeId) -> Option<u128> {
    match m.nodes.get(id as usize) {
        Some(Node::Constant { value, width }) => Some(value & bitmask(*width)),
        _ => None,
    }
}

fn casez_pattern_matches(sel: u128, pattern_value: u128, wildcard_mask: u128, width: u32) -> bool {
    let care_mask = bitmask(width) & !wildcard_mask;
    (sel & care_mask) == (pattern_value & care_mask)
}

fn eval_static_for_fold(kind: ForFoldKind, trip_count: u32, chunk_width: u32, src: u128) -> u128 {
    let mask = bitmask(chunk_width);
    let mut acc = match kind {
        ForFoldKind::And => mask,
        ForFoldKind::Xor | ForFoldKind::Or | ForFoldKind::Add => 0,
    };
    for i in 0..trip_count {
        let shift = i.saturating_mul(chunk_width);
        let chunk = if shift >= 128 {
            0
        } else {
            (src >> shift) & mask
        };
        acc = match kind {
            ForFoldKind::Xor => acc ^ chunk,
            ForFoldKind::Or => acc | chunk,
            ForFoldKind::And => acc & chunk,
            ForFoldKind::Add => acc.wrapping_add(chunk) & mask,
        };
    }
    acc & mask
}

fn render_casez_pattern(value_id: NodeId, wild_mask_id: NodeId, m: &Module) -> String {
    let Node::Constant {
        width,
        value: pattern_value,
    } = m.nodes[value_id as usize]
    else {
        unreachable!("casez patterns must be constant nodes");
    };
    let Node::Constant {
        width: mask_width,
        value: wildcard_mask,
    } = m.nodes[wild_mask_id as usize]
    else {
        unreachable!("casez wildcard masks must be constant nodes");
    };
    debug_assert_eq!(width, mask_width);
    let mut bits = String::with_capacity(width as usize);
    for bit in (0..width).rev() {
        let mask_bit = (wildcard_mask >> bit) & 1;
        let value_bit = (pattern_value >> bit) & 1;
        bits.push(if mask_bit != 0 {
            '?'
        } else if value_bit != 0 {
            '1'
        } else {
            '0'
        });
    }
    format!("{width}'b{bits}")
}

fn for_fold_symbol(kind: &ForFoldKind) -> &'static str {
    match kind {
        ForFoldKind::Xor => "^",
        ForFoldKind::Or => "|",
        ForFoldKind::And => "&",
        ForFoldKind::Add => "+",
    }
}

fn for_fold_init_literal(kind: &ForFoldKind, width: u32) -> String {
    let value = match kind {
        ForFoldKind::And => bitmask(width),
        ForFoldKind::Xor | ForFoldKind::Or | ForFoldKind::Add => 0,
    };
    const_literal(width, value)
}

fn bitmask(width: u32) -> u128 {
    if width >= 128 {
        u128::MAX
    } else {
        (1u128 << width) - 1
    }
}

fn const_literal(width: u32, value: u128) -> String {
    format!("{width}'h{:x}", value & bitmask(width))
}

